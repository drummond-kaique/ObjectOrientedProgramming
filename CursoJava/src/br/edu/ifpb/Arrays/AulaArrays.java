package br.edu.ifpb.Arrays;

public class AulaArrays {

    public static void main(String[] args) {

        String[] nomes = new String[4];

        nomes[0] = "Rafael";
        nomes[1] = "Danilo";
        nomes[2] = "Getulio";
        nomes[3] = "Dayane";
        for (String aux:nomes) {
            System.out.println(aux);
        }
    }

}
