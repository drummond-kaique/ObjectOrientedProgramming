package br.edu.ifpb;

public class AulaArrays {

    public static void main(String[] args) {

        String[] nomes = new String[10];
        nomes[1] = "Rafael";
        nomes[2] = "Danilo";
        nomes[3] = "Getulio";
        nomes[4] = "Dayane";
        for (int i = 0; i < 10; i++) {
            System.out.println(nomes[i]);
        }
    }

}
